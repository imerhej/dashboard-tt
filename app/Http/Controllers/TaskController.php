     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required|min:3|max:100'
        ]);

        $task = new Task();

        $task->job_id = $request->job_id;
        $task->name = $request->name;
        $task->save();

        $id = $request->job_id;
        return redirect()->route('jobs.show', $id);

    }

